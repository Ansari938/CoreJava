package com.raystech.switchcase;

public class switchcase {
    public static void main(String[] args) {
		char r = 'a';
		switch (r){
		case 'a':
		System.out.println("vowels");
			break;
		case 'e':
		System.out.println("vowels");
			break;
		case 'i':
		System.out.println("vowels");
			break;
		case 'o':
		System.out.println("vowels");
			break;
		case 'u':
		System.out.println("vowels");
			break;
		default:
			System.out.println("consonant");
		
		}
    }
}
		